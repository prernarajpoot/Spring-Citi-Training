package com.citi.spec;
import java.lang.Math; 
/**
 * @author indianrenters
 *
 */
public class Calc {
 
	static public int add(int a,int b)
	{
		return a + b;
	}
   static public int sub(int a,int b)
   {
	   return a - b;
   }
   static public int mul(int a,int b)
   {
	   return a * b;
   }
   static public double sinfunc(float a) 
   { 
 
     return Math.sin(a);
   } 
   public static double sqrtt(float a) 
   { 
     
       return Math.sqrt(a);
   }
   
}
